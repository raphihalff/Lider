      <div class="title">
        The Circus
    </div>
    Tell me, brother, our <span class="dog">dog's</span> struggle<br>
What does it mean?<br>
Our heart's gone mad.<br>
All <span class="word">words</span> fled —<br>
Bees from a hive embraced by smoke.<br>
But in a backstreet of the mind,<br>
Still throbs<br>
A flickering nerve saved from destruction,<br>
A last groan<br>
Defying that blind silence<br>
Sealed by a handful of earth.<br>
<br>
Who are we? What is<br>
The sense of our suffering?<br>
If only<br>
To be victims of a bloodthirsty lord —<br>
Let frogs be born instead of us!<br>
The tongue is swollen with the rusty promise<br>
That wolf and lamb will dwell together.<br>
As a child resembles father-mother, we<br>
Inherit the resemblances of generations' plague,<br>
Of being waiters at the world's set table,<br>
Grateful for a coin tossed to us.<br>
Is this the golden chain that binds two thousand years,<br>
The tear chain burdening our souls?<br>
<br>
It seems, just yesterday, forms lost their measure,<br>
Abysses straightened out their hunchback necks<br>
And covered the unburied skulls of generations <br>
With hope —<br>
&emsp;And we were ready<br>
To accept the blooming wounds as medals,<br>
Boast of them in a pagan parade:<br>
— Ho, ho, we too take part in the dream-plundering,<br>
With our blood, pay dues to revolutions!<br>
We, we.<br>
But a lion overlooks the branch-covered pit<br>
Lurking at his paws.<br>
<br>
Today — at dusk, in a circle,<br>
Around bonfire's coppery wings,<br>
Under whips of steel guards,<br>
At the laughter of yesterday's comrade —<br>
Naked, with striped backs,<br>
We dance: I in the middle.<br>
With our own hands, we are forced<br>
To tear the silver parchment<br>
And toss into the bonfire<br>
Like our own limbs, singing<br>
Happy Russian songs.<br>
Look! Between sword and sheath<br>
The voice of paradise looms,<br>
<span class="word">Letters</span> from Babylon flutter,<br>
Inscribed on the blackboard of night.<br>
And farther, on high coils —<br>
Rising, the <span class="word">"I am"</span><br>
From a consumed parchment,<br>
And nothing —<br>
He too went up in smoke.<br><br>
*<br>
Circle, circle, dancing round,<br>
&emsp;If you have a feeling — burn it,<br>
&emsp;If there is a bath in hell,<br>
This is where it will be found.<br>
<br>
And without a stick the lame<br>
&emsp;And the rabbi — blind and old,<br>
&emsp;They're all hopping in a ring<br>
For the joy and for the game.<br>
<br>
Peasant woman hops to see:<br>
&emsp;What a circus, God is One!<br>
&emsp;Says a neighbor: Pay them stones,<br>
For a circus costs a fee.<br>
<br>
One whore points out in the clatter<br>
&emsp;To another: See them naked!<br>
&emsp;Stones are falling. Fire devours.<br>
Climbs a sheygets on a ladder.<br>
<br>
Rabbi falls with stones that fall,<br>
&emsp;Kissing sparks in ashes flying.<br>
&emsp;And his Sh'ma is drowning too<br>
In the coldness of the All.<br><br>
* <br>
And I, who was the clown in that disgraceful spectacle,<br>
Had no courage to stammer a curse,<br>
No strength to throw myself into the death,<br>
As did my brothers in the time of Hadrian the Roman<br>
When faith stifled in their body all the pain<br>
(Though my heart is poisoned with coal glow<br>
And the eyes of my spirit are speared with smoke).<br>
Worse: I knelt naked before him,<br>
Who defied my father in his grave,<br>
And with tears like black pox,<br>
I begged for mercy.<br>
<br><br>
&emsp;Cursed one! Where is your old shield<br>
&emsp;That bent the spears of nations?<br>
&emsp;The colors of that image, don't they reach you?<br>
&emsp;The blood of your forefathers, was it never revealed?<br>
<br>
&emsp;This is your punishment: to gasp half dead,<br>
&emsp;Gulping death rattles of your brothers, insane.<br>
&emsp;For you have not deserved the last bread<br>
&emsp;Of joy: being naught — which means: becoming again.<br>
