      </div>
      זאָג מיר, ברודער מײַנער, זאָג,<br>
װאָס איז ער, װאָס באַטײַט ער, אונדזער <span class="dog">הינטישער</span> געראַנגל?<br>
דאָס האַרץ איז דול.<br>
אַנטלאָפֿן זענען אַלע שײנע <span class="word">װערטער</span>,<br>
גלײַך בינען פֿון אַ בינשטאָק אַ באַגאַרטלטער מיט רױך. <br>
נאָר ערגעץ אין אַ הינטערגעסל פֿון באַװוּסטזײַן שלאָגט נאָך <br>
אַ <span class="word">צוקנדיקער</span> פּיצל נערװ געראַטעװעט פֿון חורבן, <br>
אַ לעצטער קרעכץ װאָס װידערשפּעניקט יענע בלינדע שטילקײט<br>
װאָס װערט <span class="word">פֿאַרחתמעט</span> מיט אַ הױפֿן ערד. <br>
<br>
װער זענען מיר? <br>
װאָס איז דער זין פֿון אַלע אונדזערע לײַדן?<br>
אױב נאָר צו זײַן קרבנות פֿאַר אַ בלוטדאָרשטיקן האַר,<br>
דענצמאָל זאָלן אַנשטאָט אונדז געבױרן װערן זשאַבעס!<br>
די צונג איז אױפֿגעכלאָטערט פֿון דער זשאַװערדיקער טרײסט,<br>
אַז װאָלף און לאַם װעט הױערן צוזאַמען.<br>
און װי עס ירשנט ענלעכקײט דאָס קינד פֿון טאַטע־מאַמע — <br>
ירשען מיר די ענלעכקײט פֿון דורותדיקער פּלאָג,<br>
פֿון זײַן פֿאַרקנעכטע קעלנערס בײַ דער װעלטס געגרײטן טיש<br>
און <span class="dog">הינטיש</span> דאַנקען פֿאַר דער צוגעװאָרפֿענער מטבע.<br>
דאָס איז די קײט די גאָלדענע װאָס בינדט צװײ טױזנט יאָר, <br>
די טרערן־קײט אױף אונדזערע נשמות.<br>
<br>
אָט נעכטן, דאַכט זיך, האָבן פֿאָרמען זײער מאָס פֿאַרלױרן<br>
און תּהומען האָבן אױסגעגלײַכט די הױקערדיקע קערק<br>
פֿאַרדעקנדיק די ניט־באַרוטע שאַרבנס פֿון די דורות<br>
מיט האָפֿונג —<br>
&emsp;&emsp;&emsp;און מיר זענען גרײט געװען<br>
די בליִענדיקע װוּנדן אָנצונעמען פֿאַר מעדאַלן<br>
און שטאָלצעװען מיט זײ אין פֿרײלעך־געציקן פּאַראַד:<br>
— האָהאָ, מיר האָבן אױך אַ חלק אין דעם חלום־זאַקרױב,<br>
מיר צאָלן אױכעט צינדז מיט אונדזער בלוט די רעװאָלוציעס!<br>
מיר, מיר.<br>
נאָר אױך דער לײב דערזעט ניט די באַצװײַגטע גרוב<br>
װאָס לױערט אױפֿן גאַנג פֿון זײַנע לאַפּעס.<br>
<br>
&emsp;און  הײַנט — — ערבֿ נאַכט, אין אַן <span class="rhyme">עיגול</span>,<br>
&emsp;אַרום שײַטערס געקופּערטע <span class="rhyme">פֿליגל</span>,<br>
&emsp;אונטער בײַטשן פֿון שטאָלענע <span class="rhyme">װעכטער</span>,<br>
&emsp;אױף דעם נעכטיקן חבֿרס <span class="rhyme">געלעכטער</span>,<br>
&emsp;און נאַקעט, מיט פּלײצעס <span class="rhyme">צעשניטן</span>,<br>
&emsp;טאַנצן מיר: איך אין דער <span class="rhyme">מיטן</span>. <br>
&emsp;און מיט די אײגענע הענט מיר <span class="rhyme">באַדאַרפֿן</span> <br>
&emsp;רײַסן דעם זילבערנעם פּאַרמעט און <span class="rhyme">װאַרפֿן</span> <br>
&emsp;אין שײַטער אַרײַן, װי די אײגענע <span class="rhyme">גלידער</span>, <br>
&emsp;זינגענדיק פֿרײלעכע רוסישע <span class="rhyme">לידער</span>. <br>
&emsp;און זע! צװישן שװערדן און <span class="rhyme">שײדן</span><br>
&emsp;טראָגט זיך דער קול פֿון <span class="rhyme">גן־עדן</span>,<br>
&emsp;פֿלאַטערן <span class="word">אותיות</span> פֿון <span class="rhyme">בבֿל</span><br>
&emsp;און בלײַבן אױף נאַכטיקן <span class="rhyme">טאָװל</span>. <br>
&emsp;און װײַטער, אױף קנױלן אױף <span class="rhyme">הױכע</span>,<br>
&emsp;רײַסט זיך אַרױף דער <span class="word, rhyme">אָנכי</span> <br>
&emsp;פֿון גיריק פֿאַרשלונגענעם <span class="rhymeffect">פּאַרמעט</span><br>
&emsp;און <span class="rhymeffect">גאָרניט —</span> <br>
&emsp;אױך ער איז <span class="rhymeffect">דערװײַטערט</span>.<br><br>
&emsp;*<br>
&emsp;דרײ זיך, דרײ זיך קאַראַהאָד,<br>
&emsp;&emsp;האָסט נאָך אַ געפֿיל — פֿאַרברען אים.<br>
&emsp;&emsp;אױב ס׳איז דאָ אַ באָד אין גיהנום<br>
&emsp;זעט דאָרט אױס אזױ די באָד.<br>
<br>
&emsp;און דער לאָמער אָן אַ שטעקן<br>
&emsp;&emsp;און דער רבֿ – דער בלינדער גרײַז,<br>
&emsp;&emsp;אַלע האָפּקען אין אַ קרײַז<br>
&emsp;פֿאַר דער פֿרײד פֿון אַלע עקן.<br>
<br>
&emsp;שטײט אַ פּױערטע און קװעלט:<br>
&emsp;&emsp;אָט אַ צירק, װי גאָט איז אײנער!<br>
&emsp;&emsp;מאַכט איר שכן: צאָלט זײ שטײנער,<br>
&emsp;װײַל אין צירק דאַרף קאָסטן געלט.<br>
<br>
&emsp;און אַ נפֿקא װײַזט דער צװײטער <br>
&emsp;&emsp;אױף דער נאַקעטקײט: דו זעסט?<br>
&emsp;&emsp;פֿאַלן שטײנער. ס׳פֿײַער פֿרעסט.<br>
&emsp;קריכט אַ שײגעץ אױף אַ לײטער.<br>
<br>
&emsp;פֿאַלן שטײנער. פֿאַלט דער רבֿ<br>
&emsp;&emsp;קושנדיק אין אַש די פֿונקען.<br>
&emsp;&emsp;און זײַן שמע װערט אױך פֿאַרזונקען<br>
&emsp;אין דער קאַלטקײט פֿון אֵין־סוף.<br><br>
*<br>
און איך װאָס בין געװען דער לץ אין שענדלעכן ספּעקטאַקל<br>
האָב ניט געהאַט קײן מוטװיל צו אַרױסשטאַמלען אַ קללה<br>
און ניט צו מאָל דעם כּוח זיך אַ װאָרף צו טאָן אין טױט, <br>
װי מײַנע ברידער אין דער צײַט פֿון אַדריִאַן דער רױמער,<br>
בשעת דער גלױבן האָט דערשטיקט אין קערפּער די יסורים<br>
(כאָטש אַלץ אײנס איז דורכגעסמט מיט קױלנגלי מײַן האַרץ<br>
און די אױגן פֿון מײַן גײַסט מיט רױך אַדורכגעשטאָכן).<br>
נאָר מערער נאָך: איך האָב געקניט אַ נאַקעטער פֿאַר דעם <br>
װאָס האָט געשענדט מײַן טאַטן אין זײַן קבֿר<br>
און מיט טרערן װי מיט שװאַרצע פּאָקן<br>
געבעטן גנאָד.<br>
<br><br>
&emsp;פֿאַרשאָלטענער! װוּ איז דײַן אַלטע שילד <br>
&emsp;װאָס האָט צעבױגן פֿעלקערלעכע שפּיזן?<br>
&emsp;דערגרײכט דיר נאָט קײן פֿאַרב פֿון יענעם בילד, <br>
&emsp;האָט קײן מאָל זיך דײַן אָפּשטאַם ניט באַװיזן?<br>
<br>
&emsp;איז דאָס דײַן שטראָף צו זשיפּען האַלב געטײט <br>
&emsp;און פֿרעסן גסיסה־כאָרכל פֿון די ברידער.<br>
&emsp;װײַל דו האָסט ניט פֿאַרדינט די לעצטע פֿרײד<br>
